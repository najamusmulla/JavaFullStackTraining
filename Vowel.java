package JavaWeek2Lab;

public class Vowel {
	public static void main(String[] args) {
		char ch = 'k';
		
		if (ch == 'a' || ch == 'e' || ch == 'i' || ch == 'o' || ch == 'u') {
			System.out.println(ch + " is a Vowel");
			
		} else {
			System.out.println(ch + " is a Consonant");
		}
	}
}
